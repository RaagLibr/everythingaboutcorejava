package com.enfy.loopex;

public class LoopEx3 {
	public static void main(String args[])
	{
		int a=10,b=3,c=712;
		
		for(int i=1;i<=6;++i)
		{
			a=a*2;
			b=b+13*i;
			c=c+19*i*2;
			
		}
		System.out.println("a="+a);
		System.out.println("b="+b);
		System.out.println("c="+c);
		
	}
}
